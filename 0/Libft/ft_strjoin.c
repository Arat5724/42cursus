#include "libft.h"
#include <stdlib.h>

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	len1;
	size_t	len2;
	char	*result;

	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	result = (char *)malloc(sizeof(char) * (len1 + len2 + 1));
	if (!result)
		return (0);
	ft_memcpy(result, s1, sizeof(char) * len1);
	ft_memcpy(result + len1, s2, sizeof(char) * (len2 + 1));
	return (result);
}
